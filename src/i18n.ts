export type TKey = keyof typeof T.en

export function useT() {
  const lang = useStore(s => s.profile.settings.language ?? 'en') as Lang
  return function t<K extends TKey>(key: K): typeof T.en[K] {
    return (T[lang] as typeof T.en)[key] ?? T.en[key]
  }
}
